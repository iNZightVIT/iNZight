    wi <- which(
        sapply(seq_len(tbl$get_dim()[["nrow"]]),
            function(i) {
                z <- svalue(tbl[i,1]) == "Resize points by :"
                length(z) && z
            }
        )
    )
    ci <- which(
        sapply(tbl$child_positions, function(x) identical(x$child, tbl[wi, 3]))
    )
    sizeby <- tbl$children[[ci]]
